class interchange{
public static void main(String[] args){

System.out.println("Two Variables");
int x=7;
int y=9;

System.out.println("Before Swapping");
System.out.println("x ="+x);
System.out.println("y ="+y);

y = y-x;
x = x+y;

System.out.println("After Swapping");

System.out.println("x ="+x);
System.out.println("y ="+y);

}



}